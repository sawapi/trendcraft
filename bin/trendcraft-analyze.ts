// ---------------------------------------------------------------------------
// Args
// ---------------------------------------------------------------------------

function parseArgs(args: string[]): {
  csvPath?: string;
  outputDir?: string;
  help: boolean;
  prompt: boolean;
} {
  const result = {
    csvPath: undefined as string | undefined,
    outputDir: undefined as string | undefined,
    help: false,
    prompt: false,
  };

  for (const arg of args) {
    if (arg === "--help" || arg === "-h") {
      result.help = true;
    } else if (arg === "--prompt" || arg === "-p") {
      result.prompt = true;
    } else if (!arg.startsWith("-")) {
      if (!result.csvPath) {
        result.csvPath = arg;
      } else if (!result.outputDir) {
        result.outputDir = arg;
      }
    }
  }

  return result;
}

