// 2. signals.txt - Cross / Divergence / BB Squeeze
// ---------------------------------------------------------------------------

function generateSignals(candles: NormalizedCandle[]): string {
  const crossSignals = validateCrossSignals(candles, { short: 5, long: 25 });
  const divSignals = rsiDivergence(candles);
  const squeezeSignals = bollingerSqueeze(candles);

  const lines: string[] = [];

  // GC/DC History
  lines.push("=== GC/DC History (SMA 5/25) ===");
  if (crossSignals.length === 0) {
    lines.push("  (none detected)");
  } else {
    const sorted = [...crossSignals].reverse();
    const now = new Date();
    for (const c of sorted) {
      const date = formatDate(c.time);
      const typeLabel = c.type === "golden" ? "GC" : "DC";
      let detail: string;
      if (c.details.daysUntilReverse === null) {
        const daysSince = Math.round(
          (now.getTime() - c.time) / (1000 * 60 * 60 * 24),
        );
        detail = `(active, ${daysSince} days)`;
      } else {
        const fake = c.isFake ? " (fake)" : "";
        detail = `-> reversed in ${c.details.daysUntilReverse}d${fake}`;
      }
      lines.push(`  ${date}  ${typeLabel}  score:${c.score}  ${detail}`);
    }
  }

  lines.push("");
  lines.push("=== Divergence ===");
  if (divSignals.length === 0) {
    lines.push("  (none detected)");
  } else {
    const sorted = [...divSignals].reverse();
    for (const d of sorted) {
      const date = formatDate(d.time);
      lines.push(
        `  ${date}  ${d.type}  RSI  price:[${d.price.first.toFixed(0)}, ${d.price.second.toFixed(0)}]  ind:[${d.indicator.first.toFixed(1)}, ${d.indicator.second.toFixed(1)}]`,
      );
    }
  }

  lines.push("");
  lines.push("=== BB Squeeze ===");
  if (squeezeSignals.length === 0) {
    lines.push("  (none detected)");
  } else {
    const sorted = [...squeezeSignals].reverse();
    for (const s of sorted) {
      const date = formatDate(s.time);
      lines.push(
        `  ${date}  bandwidth:${(s.bandwidth * 100).toFixed(1)}%  percentile:${Math.round(s.percentile)}`,
      );
    }
  }

  return lines.join("\n");
}

