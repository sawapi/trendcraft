function generateReadings(candles: NormalizedCandle[]): string {
  const sma25 = sma(candles, { period: 25 });
  const sma75 = sma(candles, { period: 75 });
  const bb = bollingerBands(candles);
  const rsiData = rsi(candles);
  const macdData = macd(candles);
  const obvData = obv(candles);
  const volAnomaly = volumeAnomaly(candles);

  // Build lookup maps by time
  const sma25Map = new Map(sma25.map((s) => [s.time, s.value]));
  const sma75Map = new Map(sma75.map((s) => [s.time, s.value]));
  const bbMap = new Map(bb.map((b) => [b.time, b.value]));
  const rsiMap = new Map(rsiData.map((r) => [r.time, r.value]));
  const macdMap = new Map(macdData.map((m) => [m.time, m.value]));
  const obvMap = new Map(obvData.map((o) => [o.time, o.value]));
  const volMap = new Map(volAnomaly.map((v) => [v.time, v.value]));

  // Take last 20 candles
  const recent = candles.slice(-20);

  const rows: Record<string, string | number | null>[] = [];
  for (const c of recent) {
    const t = c.time;
    const s25 = sma25Map.get(t);
    const s75 = sma75Map.get(t);
    const bbVal = bbMap.get(t);
    const rsiVal = rsiMap.get(t);
    const macdVal = macdMap.get(t);
    const obvVal = obvMap.get(t);
    const volVal = volMap.get(t);

    // Derived metrics
    const sma25Dev =
      s25 != null ? Math.round(((c.close - s25) / s25) * 10000) / 100 : null;
    const sma75Dev =
      s75 != null ? Math.round(((c.close - s75) / s75) * 10000) / 100 : null;

    let bbPctB: number | null = null;
    let bbBW: number | null = null;
    if (bbVal) {
      const { upper, lower, middle } = bbVal;
      if (upper != null && lower != null && upper !== lower) {
        bbPctB = Math.round(((c.close - lower) / (upper - lower)) * 100) / 100;
      }
      if (upper != null && lower != null && middle != null && middle !== 0) {
        bbBW = Math.round(((upper - lower) / middle) * 10000) / 100;
      }
    }

    rows.push({
      Date: formatDate(t),
      Close: c.close,
      "SMA25 Dev%": sma25Dev,
      "SMA75 Dev%": sma75Dev,
      "BB %B": bbPctB,
      "BB BW%": bbBW,
      RSI: rsiVal ?? null,
      "MACD Hist": macdVal?.histogram ?? null,
      OBV: obvVal ?? null,
      "Vol Ratio": volVal?.ratio ?? null,
    });
  }

  return formatTable(rows);
}

