// CSV Parser (inline, supports PER/PBR - based on chart-viewer's parseCSV)
// ---------------------------------------------------------------------------

function parseCsvWithFundamentals(text: string): ParseResult {
  const lines = text.trim().split("\n");
  const candles: NormalizedCandle[] = [];
  const perValues: (number | null)[] = [];
  const pbrValues: (number | null)[] = [];
  let hasFundamentals = false;

  for (let i = 1; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;

    const parts = line.split(",");
    if (parts.length < 6) continue;

    const [dateStr, openStr, highStr, lowStr, closeStr, volumeStr, adjCloseStr, perStr, pbrStr] =
      parts;

    const dateParts = dateStr.split(/[/\-]/);
    if (dateParts.length !== 3) continue;

    const date = new Date(
      Number.parseInt(dateParts[0]),
      Number.parseInt(dateParts[1]) - 1,
      Number.parseInt(dateParts[2]),
    );

    if (Number.isNaN(date.getTime())) continue;

    const rawClose = Number.parseFloat(closeStr);
    const adjustedClose = adjCloseStr ? Number.parseFloat(adjCloseStr) : rawClose;
    const adjustmentRatio = adjustedClose / rawClose;

    candles.push({
      time: date.getTime(),
      open: Number.parseFloat(openStr) * adjustmentRatio,
      high: Number.parseFloat(highStr) * adjustmentRatio,
      low: Number.parseFloat(lowStr) * adjustmentRatio,
      close: adjustedClose,
      volume: Number.parseFloat(volumeStr),
    });

    const per = perStr ? Number.parseFloat(perStr) : null;
    const pbr = pbrStr ? Number.parseFloat(pbrStr) : null;
    perValues.push(per !== null && !Number.isNaN(per) ? per : null);
    pbrValues.push(pbr !== null && !Number.isNaN(pbr) ? pbr : null);

    if (per !== null && !Number.isNaN(per)) hasFundamentals = true;
    if (pbr !== null && !Number.isNaN(pbr)) hasFundamentals = true;
  }

  // Sort by time ascending with fundamentals
  const indexed = candles.map((c, i) => ({ candle: c, per: perValues[i], pbr: pbrValues[i] }));
  indexed.sort((a, b) => a.candle.time - b.candle.time);

  return {
    candles: indexed.map((item) => item.candle),
    fundamentals: hasFundamentals
      ? { per: indexed.map((item) => item.per), pbr: indexed.map((item) => item.pbr) }
      : null,
  };
}

