function generatePatterns(candles: NormalizedCandle[]): string {
  const TWO_YEARS_MS = 2 * 365 * 24 * 60 * 60 * 1000;
  const cutoff = candles[candles.length - 1].time - TWO_YEARS_MS;

  const patterns = [
    ...headAndShoulders(candles),
    ...inverseHeadAndShoulders(candles),
    ...doubleTop(candles),
    ...doubleBottom(candles),
    ...cupWithHandle(candles),
  ].filter((p) => p.time >= cutoff);

  // Sort by time descending
  patterns.sort((a, b) => b.time - a.time);

  const lines: string[] = ["=== Chart Patterns ==="];

  if (patterns.length === 0) {
    lines.push("  (none detected)");
  } else {
    for (const p of patterns) {
      const date = formatDate(p.time);
      const confirmed = p.confirmed ? "yes" : "no";
      lines.push(
        `  ${p.type}  ${date}  confidence:${Math.round(p.confidence)}  confirmed:${confirmed}`,
      );

      const parts: string[] = [];
      if (p.pattern.target != null) parts.push(`target: ${p.pattern.target.toFixed(0)}`);
      if (p.pattern.stopLoss != null) parts.push(`stopLoss: ${p.pattern.stopLoss.toFixed(0)}`);
      if (p.pattern.height != null) parts.push(`height: ${p.pattern.height.toFixed(0)}`);
      if (parts.length > 0) {
        lines.push(`    ${parts.join("  ")}`);
      }

      // Key points
      if (p.pattern.keyPoints.length > 0) {
        const kpStr = p.pattern.keyPoints
          .map((kp) => `${kp.label}: ${formatDate(kp.time)} ${kp.price.toFixed(0)}`)
          .join(" | ");
        lines.push(`    ${kpStr}`);
      }
    }
  }

  return lines.join("\n");
}

// ---------------------------------------------------------------------------
// 4. valuation.txt - PER/PBR/ROE with SMA and percentiles
// ---------------------------------------------------------------------------

function generateValuation(fundamentals: FundamentalData): string {
  const { per, pbr } = fundamentals;
  const len = per.length;

  // Compute ROE = (PBR / PER) * 100
  const roe: (number | null)[] = [];
  for (let i = 0; i < len; i++) {
    if (per[i] !== null && pbr[i] !== null && per[i] !== 0) {
      roe.push((pbr[i]! / per[i]!) * 100);
    } else {
      roe.push(null);
    }
  }

  const perSma = nullableSma(per, 20);
  const pbrSma = nullableSma(pbr, 20);
  const roeSma = nullableSma(roe, 20);

  const lastIdx = len - 1;

  const entries: {
    label: string;
    current: number | null;
    sma20: number | null;
    pct: number | null;
  }[] = [
    {
      label: "PER",
      current: per[lastIdx],
      sma20: perSma[lastIdx],
      pct: percentile(per, lastIdx, 252),
    },
    {
      label: "PBR",
      current: pbr[lastIdx],
      sma20: pbrSma[lastIdx],
      pct: percentile(pbr, lastIdx, 252),
    },
    {
      label: "ROE",
      current: roe[lastIdx],
      sma20: roeSma[lastIdx],
      pct: percentile(roe, lastIdx, 252),
    },
  ];

  const lines = ["=== Valuation Summary ==="];
  lines.push("        Current  SMA(20)  Percentile  Level");

  for (const e of entries) {
    if (e.current === null) continue;
    const label = e.label.padStart(5);
    const current = e.current.toFixed(2).padStart(7);
    const smaStr = e.sma20 != null ? e.sma20.toFixed(2).padStart(7) : "      -";
    const pctStr = e.pct != null ? `${e.pct}%`.padStart(10) : "         -";
    const level = percentileLevel(e.pct);
    lines.push(`  ${label}  ${current}  ${smaStr}  ${pctStr}        ${level}`);
  }

  return lines.join("\n");
}

// ---------------------------------------------------------------------------
// 5. recent-data.txt - Last 20 days OHLCV from CSV
// ---------------------------------------------------------------------------

function generateRecentData(csvPath: string): string {
  const raw = readFileSync(csvPath, "utf-8");
  const lines = raw.trim().split("\n");

  const rows: string[][] = [];
  for (let i = 1; i < lines.length; i++) {
    const cols = lines[i].trim().split(",");
    if (cols.length < 6) continue;
    rows.push(cols);
  }

  // Sort by date descending
  rows.sort(
    (a, b) =>
      new Date(b[0].replace(/\//g, "-")).getTime() -
      new Date(a[0].replace(/\//g, "-")).getTime(),
  );

  const recent = rows.slice(0, 20);
  const hasFundamentals = recent.some((r) => r[7] && !Number.isNaN(Number.parseFloat(r[7])));

  let header = "Date        | Open    | High    | Low     | Close   | Volume      | Adj Close";
  if (hasFundamentals) header += " | PER    | PBR";
  const sep = "-".repeat(header.length);

  const out = [header, sep];
  for (const r of recent) {
    let line = [
      (r[0] || "").padEnd(11),
      (r[1] || "").padStart(7),
      (r[2] || "").padStart(7),
      (r[3] || "").padStart(7),
      (r[4] || "").padStart(7),
      (r[5] || "").padStart(11),
      (r[6] || "").padStart(9),
    ].join(" | ");
    if (hasFundamentals) {
      line += " | " + (r[7] || "").padStart(6) + " | " + (r[8] || "").padStart(4);
    }
    out.push(line);
  }
  return out.join("\n");
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

async function main(): Promise<void> {
  const args = process.argv.slice(2);
  const options = parseArgs(args);

  if (options.help) {
    printHelp();
    process.exit(0);
  }

  if (options.prompt) {
    printPrompt();
    process.exit(0);
  }

  if (!options.csvPath) {
    console.error("Error: CSV file path is required\n");
    printHelp();
    process.exit(1);
  }

  const csvPath = resolve(options.csvPath);
  if (!existsSync(csvPath)) {
    console.error(`Error: File not found: ${csvPath}`);
    process.exit(1);
  }

  const symbol = basename(csvPath, ".csv");
  const outputDir = resolve(options.outputDir ?? `/tmp/analysis/${symbol}`);
  mkdirSync(outputDir, { recursive: true });

  console.log(`=== TrendCraft Analysis: ${symbol} ===`);
  console.log(`Output: ${outputDir}/`);
  console.log();

  // Parse CSV
  process.stderr.write("[1/6] Parsing CSV...\n");
  const csvText = readFileSync(csvPath, "utf-8");
  const { candles, fundamentals } = parseCsvWithFundamentals(csvText);
  console.log(`  ${candles.length} candles loaded`);

  // 1. Indicator readings
  process.stderr.write("[2/6] Calculating indicators...\n");
  const readings = generateReadings(candles);
  writeFileSync(`${outputDir}/readings.txt`, readings);

  // 2. Signals
  process.stderr.write("[3/6] Detecting signals...\n");
  const signals = generateSignals(candles);
  writeFileSync(`${outputDir}/signals.txt`, signals);

  // 3. Patterns
  process.stderr.write("[4/6] Detecting chart patterns...\n");
  const patterns = generatePatterns(candles);
  writeFileSync(`${outputDir}/patterns.txt`, patterns);

  // 4. Valuation
  if (fundamentals) {
    process.stderr.write("[5/6] Calculating valuation...\n");
    const valuation = generateValuation(fundamentals);
    writeFileSync(`${outputDir}/valuation.txt`, valuation);
  } else {
    process.stderr.write("[5/6] Valuation: Skipped (no PER/PBR data)\n");
  }

  // 5. Recent data + status
  process.stderr.write("[6/6] Saving recent data & status...\n");
  writeFileSync(`${outputDir}/recent-data.txt`, generateRecentData(csvPath));

  // 6. Status JSON
  const firstCandle = candles[0];
  const lastCandle = candles[candles.length - 1];
  const status = {
    symbol,
    dateRange: `${formatDate(firstCandle.time)} - ${formatDate(lastCandle.time)}`,
    candleCount: candles.length,
    hasFundamentals: fundamentals !== null,
  };
  writeFileSync(`${outputDir}/status.json`, JSON.stringify(status, null, 2));

  console.log();
  console.log("=== Done ===");
  console.log();
  console.log("Files:");
  console.log(`  ${outputDir}/readings.txt`);
  console.log(`  ${outputDir}/signals.txt`);
  console.log(`  ${outputDir}/patterns.txt`);
  if (fundamentals) {
    console.log(`  ${outputDir}/valuation.txt`);
  }
  console.log(`  ${outputDir}/recent-data.txt`);
  console.log(`  ${outputDir}/status.json`);
}

main();
