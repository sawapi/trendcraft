// Fundamental helpers (inline - SMA and percentile for nullable arrays)
// ---------------------------------------------------------------------------

function nullableSma(values: (number | null)[], period: number): (number | null)[] {
  const result: (number | null)[] = [];
  for (let i = 0; i < values.length; i++) {
    if (i < period - 1) {
      result.push(null);
      continue;
    }
    let sum = 0;
    let count = 0;
    for (let j = i - period + 1; j <= i; j++) {
      if (values[j] !== null) {
        sum += values[j]!;
        count++;
      }
    }
    result.push(count > 0 ? sum / count : null);
  }
  return result;
}

function percentile(values: (number | null)[], idx: number, lookback: number): number | null {
  const start = Math.max(0, idx - lookback + 1);
  const valid: number[] = [];
  for (let i = start; i <= idx; i++) {
    if (values[i] !== null) valid.push(values[i]!);
  }
  if (valid.length < 10) return null;
  const current = values[idx];
  if (current === null) return null;
  const below = valid.filter((v) => v < current).length;
  return Math.round((below / valid.length) * 100);
}

function percentileLevel(pct: number | null): string {
  if (pct === null) return "-";
  if (pct <= 20) return "Very Low";
  if (pct <= 40) return "Low";
  if (pct <= 60) return "Neutral";
  if (pct <= 80) return "High";
  return "Very High";
}

