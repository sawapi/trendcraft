// Formatting helpers
// ---------------------------------------------------------------------------

function formatDate(time: number): string {
  const d = new Date(time);
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, "0");
  const day = String(d.getDate()).padStart(2, "0");
  return `${y}/${m}/${day}`;
}

function formatTable(rows: Record<string, string | number | null>[]): string {
  if (!rows.length) return "No data";
  const keys = Object.keys(rows[0]);
  const widths: Record<string, number> = {};
  for (const k of keys) {
    widths[k] = Math.max(
      k.length,
      ...rows.map((r) => {
        const v = r[k];
        if (v == null) return 1;
        return String(typeof v === "number" ? (v > 1000 ? v.toFixed(0) : v.toFixed(2)) : v).length;
      }),
    );
  }
  const header = keys.map((k) => k.padStart(widths[k])).join(" | ");
  const sep = keys.map((k) => "-".repeat(widths[k])).join("-+-");
  const lines = [header, sep];
  for (const row of rows) {
    lines.push(
      keys
        .map((k) => {
          const v = row[k];
          if (v == null) return "-".padStart(widths[k]);
          if (typeof v === "number")
            return (v > 1000 ? v.toFixed(0) : v.toFixed(2)).padStart(widths[k]);
          return String(v).padStart(widths[k]);
        })
        .join(" | "),
    );
  }
  return lines.join("\n");
}

